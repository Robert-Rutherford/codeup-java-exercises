public class StringExercise {
    public static void main(String[] args) {
//        We don't need no education
//        We don't need no thought control
        String dontNeed = "We We don't need no education\nWe don't need no thought control";
        System.out.println(dontNeed);
        System.out.println();
//        Check "this" out!, "s inside of "s!
        String parenthesise = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(parenthesise);
        System.out.println();
//        In windows, the main drive is usually C:\
        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);
        System.out.println();
//        I can do backslashes \, double backslashes \\,
//        and the amazing triple backslash \\\!
        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslash);
    }
}
